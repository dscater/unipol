        <p>
            <b>II.</b> La calificación mínima de aprobación es de cincuenta y un (51,00) puntos.
        </p>
        <p><b>III.</b> La o el postulante que obtuviere una calificación final entre cincuenta coma cincuenta (50,50) a
            cincuenta
            coma noventa y nueve (50,99), obtendrá una calificación redondeada equivalente a cincuenta y un (51,00)
            puntos,
            que representará la aprobación de su evaluación.</p>
        <p>
            <b>IV.</b> La o el postulante que obtuviere una calificación final entre cincuenta coma cero uno (50,01) a
            cincuenta
            coma cuarenta y nueve (50,49), obtendrá una calificación redondeada equivalente a cincuenta (50,00) puntos,
            que
            representará la reprobación de su evaluación.
        </p>
        <p> <b>V.</b> Los parámetros de calificación en las evaluaciones Médicas, Odontológicas y Psicológicas están
            definidos
            como
            “APTO” o “NO APTO” para continuar dentro del Proceso de Admisión.</p>

        <p>
            <b>VI.</b> Las y los postulantes que obtuvieren una calificación de reprobación o resultaren “NO APTOS”
            dentro de
            cualquiera de las Etapas de Evaluación, serán separados del Proceso de Admisión sin mayor trámite.
        </p>
        <p>
            <b> VII.</b> La Escala de Calificación para la Evaluación de Aptitud Física, será en una escala de uno
            (1,00) a cien
            (100,00) puntos, siendo la calificación mínima de aprobación de cincuenta y un (51,00) puntos.
        </p>
        <p>
            <b>VIII.</b> El rendimiento académico de las y los postulantes, será determinado mediante el promedio final
            del
            programa académico prefacultativo, obtenido de la sumatoria de los promedios de las áreas de: conocimientos,
            Instrucción Policial y Acondicionamiento Físico, cuyo resultado será dividido por la cantidad de las áreas
            antes
            descritas.
        </p>
