<ul class="list-unstyled pl-3">
    <li>
        <b>a)</b> Fase de Convocatoria
    </li>
    <li>
        <b>b)</b> Fase de Selección
    </li>
    <li>
        <b>c)</b> Fase de Incorporación
    </li>
</ul>
<b>CONCLUSIÓN DE LAS FASES DEL PROCESO DE ADMISIÓN</b>
<p>
    Cada una de las fases, procedimientos, plazos, etapas y evaluaciones establecidas en el presente Reglamento, no
    podrán
    retrotraerse a las que ya hayan finalizado, bajo el principio de preclusión de los derechos y la aceptación tácita
    de
    actos consentidos; considerando que todas las y los postulantes, han tenido la misma posibilidad y oportunidad de
    conocer, cumplir y someterse a los procedimientos y evaluaciones establecidas en el presente Reglamento en igualdad
    de
    condiciones, sin que exista favoritismo o discriminación.
</p>
<b>SEDE O SEDES DE EVALUACIÓN</b>
<p>
    La sede o sedes para las evaluaciones, serán determinadas por la Comisión Nacional del Proceso de Admisión y serán
    publicadas de manera oportuna a través de los canales de comunicación autorizados por la UNIPOL.
</p>
