        <p>
            Tiene por finalidad detectar las cualidades de personalidad, el temperamento y las características
            tipológicas
            de las y los postulantes, aptos para la profesión policial, así como de establecer el adecuado manejo de los
            valores, capacidad de liderazgo y adaptabilidad a la vida policial.
        </p>
        <b>PROCEDIMIENTO DE LA EVALUACIÓN PSICOLÓGICA</b><br />
        <p>
            <b>I.</b> La Dirección Nacional de Salud y Bienestar Social a través de su área organizacional de
            Psicología, es la
            responsable de planificar, organizar, desarrollar y ejecutar el proceso de la Evaluación Psicológica, en
            coordinación con la Comisión Nacional del Proceso de Admisión, conforme al Cronograma de Actividades,
            Reglamento
            y protocolos correspondientes.
        </p>
        <p>
            <b>II.</b> Los resultados finales de la Evaluación Psicológica, serán expresados en términos de APTO o NO
            APTO.
        </p>
        <p>
            <b>III.</b> Los costos de la Evaluación Psicológica serán cubiertos por las y los postulantes, conforme al
            Reglamento
            Interno de la Dirección Nacional de Salud y Bienestar Social.
        </p>
        <p>
            <b>IV.</b> Los Coordinadores Departamentales de Evaluación psicológica a nivel nacional dispondrán de
            profesionales
            psicólogos a cada unidad académica, a objeto de realizar el seguimiento y control de los rasgos de
            personalidad,
            conductual y comportamental de las y los postulantes, durante el desarrollo del programa académico
            prefacultativo de las unidades académicas de pregrado de la UNIPOL.
        </p>
        <b>PUBLICACIÓN DE RESULTADOS</b><br />
        <p>
            <b>I.</b> La Comisión Nacional del Proceso de Admisión, conforme el cronograma de actividades publicara los
            resultados
            de la Evaluación Psicológica, a través de la Página Web y Redes Sociales Oficiales de la Universidad
            Policial.
        </p>
        <p>
            <b>II.</b> La publicación de los resultados de la Evaluación Psicológica no consignará el nombre de la o el
            postulante,
            tan solo el número de Cedula de identidad o código de inscripción, con la finalidad de preservar la
            privacidad
            de las y los postulantes.
        </p>
    </div>
</div>
