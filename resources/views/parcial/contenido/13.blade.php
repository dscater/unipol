        <b>ADMISIÓN DIRECTA POR ESPIRITU POLICIAL</b><br />
        <p>
            <b>I.</b> Esta modalidad de Admisión Directa por Espíritu Policial, busca reconocer el esfuerzo, la
            constancia y el
            desempeño
            integral, así como también valorar y fomentar las cualidades excepcionales de vocación policial, liderazgo,
            disciplina,
            responsabilidad, adaptabilidad y compromiso institucional, demostradas durante el desarrollo del
            Prefacultativo.
            Esta
            disposición tiene la finalidad de fortalecer la calidad del proceso de admisión a las Unidades Académicas de
            Pregrado de
            la UNIPOL, promoviendo el ingreso de postulantes que no solamente cumplan con los requisitos académicos,
            sino
            también
            tengan competencias actitudinales, sociales y éticas vinculadas con el ejercicio de la función policial,
            contribuyendo
            así a la formación de futuras y futuros oficiales de policía con excelencia profesional, ética y compromiso
            social rumbo
            al Bicentenario, esta modalidad de admisión se aplicara conforme el siguiente detalle:
        </p>
        <ul class="list-unstyled ml-3">
            <li>
                <b>a. Facultad en Ciencias Policiales “Academia Nacional de Policías” (ANAPOL):</b> Cuatro (4)
                admisiones
                directas, dos
                (2)
                varones y dos (2) mujeres.
            </li>
            <li>
                <b>b. Unidades Académicas de Pregrado a Nivel de Técnico Superior (FATESCIPOL):</b> Dos (2) admisiones
                directas
                por
                cada unidad
                académica, un (1) varón y una (1) mujer.
            </li>
            <li>
                <b>c. Escuela Básica Policial de Música (ESBAPOLMUS):</b> Dos (2) admisiones directas, un (1) varón y
                una (1)
                mujer.
            </li>
        </ul>
        <p>
            <b>II.</b> <b><u>La admisión directa, NO exime a las y los postulantes beneficiados del cumplimiento de los
                    requisitos
                    generales y
                    específicos establecidos por la normativa vigente</u></b>, debiendo garantizarse en todo momento los
            principios de
            meritocracia,
            equidad, legalidad y transparencia en el proceso de selección.
        </p>
