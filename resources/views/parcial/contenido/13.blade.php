        <b>INICIO Y DURACIÓN</b><br />
        <p>
            El desarrollo del Programa Académico Prefacultativo para la Admisión a las Unidades Académicas de Pregrado,
            será:
        </p>
        <ul class="list-unstyled pl-3">
            <li>
                <b>a)</b> Conforme el cronograma de actividades aprobada por el Consejo Académico Universitario de la
                UNIPOL y
                refrendada por
                la Comisión Nacional del Proceso de Admisión.
            </li>
            <li>
                <b>b)</b> Régimen internado, el Programa Académico Prefacultativo cuenta con 380 Horas Académicas.
            </li>
        </ul>
        <b>ESPACIOS FÍSICOS</b><br />
        <p>
            El Programa Académico Prefacultativo para la Admisión a las Unidades Académicas de Pregrado de la UNIPOL, se
            ejecutará
            en instalaciones de las diferentes Unidades Académicas de Pregrado de la UNIPOL, conforme el nivel académico
            al
            que
            postulen.
        </p>
        <b>PLAZAS PARA EL PROGRAMA ACADEMICO PREFACULTATIVO</b><br />
        <p>
            <b>I.</b> Las plazas para el desarrollo del programa académico serán determinadas conforme a la capacidad
            física y
            funcional de
            las diferentes unidades académicas de pregrado.
        </p>
        <p>
            <b>II.</b> La asignación de las plazas habilitadas para las diferentes Unidades Académicas de Pregrado se
            realizará
            en
            función
            a los resultados obtenidos en las evaluaciones médica, psicológica y al orden de prelación establecido por
            los
            promedios
            finales de la Evaluación de Aptitud Física.
        </p>
        <p>
            <b>III.</b> Para la Facultad en Ciencias Policiales “Academia Nacional de Policías” (ANAPOL), se
            desarrollará de
            manera
            exclusiva en la ciudad de La Paz.
        </p>
        <p>
            <b>IV.</b> Para las diferentes unidades académicas en el nivel de técnico superior (FATESCIPOL) a nivel
            Nacional,
            en
            caso de
            que el número de postulantes al programa académico prefacultativo exceda la capacidad establecida por una
            unidad
            académica, se procederá a su redistribución hacia otra unidad académica de acuerdo a la capacidad, previo
            consentimiento
            del postulante a fin de garantizar las condiciones adecuadas para el desarrollo integral del programa
            académico
        </p>
        <p>
            <b>V.</b> Para la Escuela Básica Policial de Música (ESBAPOLMUS), se desarrollará de manera exclusiva en la
            ciudad
            de
            Cochabamba.
        </p>

        <b>EVALUACIÓN</b><br />
        <p>
            <b>I.</b> Las y los cursantes (postulantes) del prefacultativo, a la culminación de cada módulo, asignatura
            y/o
            materia
            parte
            del programa académico, de manera inmediata serán sometidos a las evaluaciones teóricas y/o practicas según
            corresponda.
        </p>
        <p>
            <b>II.</b> Todas las evaluaciones serán calificadas en una escala numérica de 1 a 100 puntos, con una nota
            mínima
            de
            aprobación
            de 51,00 puntos, y las conceptuales de “Aprobado” o “Reprobado”.
        </p>
        <p>
            <b>III.</b> Las evaluaciones del programa académico de la UNIPOL, se realizarán según las siguientes áreas:
        </p>
        <ul class="list-unstyled ml-5">
            <li>
                <b>a)</b> Conocimientos (Lengua Castellana, Matemáticas-Física, Ciencias Sociales, Historia Policial):
                Se
                realizarán de
                forma
                escrita e individual, conforme al desarrollo de cada módulo o asignatura del programa académico.
            </li>
            <li>
                <b>b)</b> Instrucción Policial: Se llevarán a cabo de forma práctica ya sea de manera individual o
                grupal,
                según los
                procedimientos y parámetros establecidos por la UNIPOL.
            </li>
            <li>
                <b>c)</b> Acondicionamiento Físico: Se realizarán de forma práctica e individual, conforme a los
                procedimientos
                y
                parámetros
                establecidos por la UNIPOL.
            </li>
        </ul>
        <p>
            <b>IV.</b> Para acceder o ser habilitado a las evaluaciones antes descritas, son requisitos imprescindibles
            las
            siguientes:
        </p>
        <ul class="list-unstyled ml-5">
            <li>
                <b>a)</b> Asistir al 100% de las actividades académicas y/o a la carga horaria de cada uno de los
                módulos,
                asignaturas
                y/o
                materia del programa académico del prefacultativo.
            </li>
            <li>
                <b>b)</b> Excepcionalmente, y por única vez a lo largo del desarrollo del programa prefacultativo, se
                considerará hasta
                un 20%
                de inasistencia a las actividades académicas y/o a la carga horaria correspondiente a cualquiera de los
                módulos,
                asignaturas y/o materias que lo integran, siempre que dicha inasistencia esté debidamente justificada y
                sea
                notificada
                por el estudiante dentro de los plazos y conforme a los procedimientos formalmente establecidos
                Académico.
            </li>
            <li>
                <b>c)</b> Haber cumplido con todos los requisitos académicos y administrativos.
            </li>
            <li>
                <b>d)</b> Cumplir con el Reglamento de Régimen Disciplinario de las Unidades Académicas de Pregrado de
                la
                Universidad
                Policial
                “Mcal. Antonio José de Sucre” (UNIPOL), específicamente en su Título VI (Sanciones Disciplinarias y su
                clasificación).
            </li>
        </ul>
        <p>
            <b>V.</b> Para determinar los promedios de las áreas de conocimientos, instrucción policial y
            acondicionamiento
            físico,
            se
            establecen los siguientes parámetros:
        </p>
        <ul class="list-unstyled ml-5">
            <li>
                <b>a)</b> Con relación a los módulos, asignaturas y/o materias de lengua castellana, matemáticas –
                física,
                ciencias
                sociales e
                historia policial, deberá realizar la sumatoria de cada una de las notas obtenidas cuyo resultado será
                promediado por la
                cantidad de materias, siendo esta el promedio final del área de conocimientos.
            </li>
            <li> <b>b)</b> Con relación al módulo, asignatura y/o materia de Instrucción Policial, la nota final
                obtenida será
                considerada como
                promedio final del área de Instrucción Policial.Con relación al módulo, asignatura y/o materia de
                Acondicionamiento
                Físico, se deberá realizar la sumatoria de cada una de las notas de las pruebas físicas obtenidas por el
                o la
                postulante, cuyo resultado deberá ser promediado por la cantidad de pruebas, siendo esta el promedio
                final del
                área de
                Acondicionamiento físico.

            </li>
        </ul>
        <p>
            <b>VI.</b> Para establecer el promedio final del programa académico prefacultativo, se realizará la
            sumatoria de
            los
            promedios
            de las áreas de: conocimientos, Instrucción Policial y Acondicionamiento Físico, cuyo resultado será
            dividido
            por la
            cantidad de las áreas, siendo esta el promedio final del rendimiento académico obtenido en el prefacultativo
            por
            la o el
            Postulante.
        </p>
        <b>EVALUACIÓN DEL ÁREA DE CONOCIMIENTOS</b><br />
        <p>
            La Evaluación del Área de Conocimientos tiene como finalidad determinar las competencias cognitivas y
            académicas
            de las
            y los postulantes, para garantizar su preparación y adaptación a la formación profesional policial en un
            proceso
            transparente, equitativo e inclusivo, conforme a los estándares establecidos por la UNIPOL.
        </p>
        <b>APLICACIÓN DE LA EVALUACIÓN DEL ÁREA DE CONOCIMIENTOS</b><br />
        <p>
            <b>I.</b> La Evaluación de Conocimientos será de selección múltiple bajo la modalidad presencial.
        </p>
        <p>
            <b>II.</b> Las evaluaciones serán de manera individual y a la culminación de cada asignatura, materia y/o
            modulo,
            conforme el
            desarrollo de estas previamente planificadas y establecidas.
        </p>
        <p>
            <b>III.</b> La cantidad de preguntas de la evaluación de cada módulo, asignatura y/o materia, será
            determinada por
            la
            Comisión
            Nacional del Proceso de Admisión, previo informe técnico del Comité Nacional de Coordinación y Control del
            Prefacultativo.
        </p>
        <p>
            <b>IV.</b> El tiempo de duración de la Evaluación de Conocimientos, será determinado por el Comité Nacional
            de
            Coordinación y
            Control del Prefacultativo en coordinación con la Comisión Nacional del Proceso de Admisión.
        </p>
        <p>
            <b>V.</b> Las evaluaciones de conocimientos serán calificadas por medios informáticos o manuales, debiendo
            plasmarse
            los
            resultados en un acta e informe escrito.
        </p>
        <p>
            <b>VI.</b> La calificación mínima de aprobación será de cincuenta y un puntos coma cero (51,00). Asimismo,
            cuando
            sea
            necesario
            se aplicará la regla de redondeo establecida en el Artículo 28 del presente reglamento.
        </p>
        <p>
            <b>VII.</b> La Comisión Nacional del Proceso de Admisión previo informe y coordinación con el Comité
            Nacional de
            Coordinación y
            Control del Prefacultativo, de acuerdo a necesidades podrá requerir el apoyo y/o tercerización de la
            Evaluación
            de
            Conocimientos a otras universidades públicas o privadas; empresas públicas y/o privadas legalmente
            constituidas.
        </p>
        <b>EVALUACIÓN DEL ÁREA DE ACONDICIONAMIENTO FÍSICO</b><br />
        <p>
            La Evaluación del área de Acondicionamiento Físico tiene por finalidad verificar las capacidades físicas,
            motrices y de
            resistencia de la o el postulante, asegurando que cumplan con los estándares requeridos para la formación
            profesional
            policial.
        </p>
        <b>APLICACIÓN DE LA EVALUACIÓN DEL ÁREA DE ACONDICIONAMIENTO FÍSICO</b><br />
        <p>
            <b>I.</b> La Evaluación de Acondicionamiento Físico, consistirá en una serie de pruebas diseñadas para medir
            diferentes
            capacidades físicas mediante ejercicios específicos, cuyos parámetros, estándares, procedimientos, ejecución
            y
            demás
            aspectos estarán definidos en los Protocolos e Instructivos de Evaluación de Acondicionamiento Físico para
            el
            Proceso de
            Admisión. Las pruebas físicas mínimas serán las siguientes:
        </p>
        <ul class="list-unstyled ml-3">
            <li>
                <b>a.</b> Carrera de cien (100) metros planos (velocidad).
            </li>
            <li>
                <b>b.</b> Fuerza de brazos: tendido dorsal para mujeres y barra alta para hombres.
            </li>
            <li>
                <b>c.</b> Salto horizontal sin impulso.
            </li>
            <li>
                <b>d.</b> Fuerza abdominal.
            </li>
            <li>
                <b>e.</b> Resistencia: carrera de tres mil (3000) metros.
            </li>
        </ul>
        <p>
            <b>II.</b> La implementación de otras evaluaciones o pruebas adicionales, serán sujetas a determinación de
            la
            Comisión
            Nacional
            del Proceso de Admisión.
        </p>
        <p>
            <b>III.</b> La <b><u>calificación mínima de aprobación será de cincuenta y un puntos coma cero
                    (51,00)</u></b>. Asimismo,
            cuando
            sea
            necesario se aplicará la regla de redondeo establecida en el Artículo 28 del presente reglamento.
        </p>
        <p>
            <b>IV.</b> La Evaluación de Acondicionamiento Físico se realizará conforme a cronograma de actividades en
            los
            lugares,
            fechas y
            horarios definidos por la Comisión Nacional del Proceso de Admisión.
        </p>
        <p>
            <b>V.</b> La o el postulante, previo a rendir la Evaluación de Acondicionamiento Físico, firmará un
            <b><u>acta de
                    consentimiento</u></b> que
            deslinde de cualquier responsabilidad a la Policía Boliviana por cualquier lesión o daño que pueda sufrir
            durante la
            evaluación, asimismo, declarará formalmente que se encuentra en óptimas condiciones físicas y de salud para
            la
            evaluación, además de declarar no haber ingerido o consumido sustancias controladas o cualquier otro tipo de
            sustancia,
            con la finalidad de mejorar o alterar su rendimiento físico.
        </p>
        <p>
            <b>VI.</b> El Acta de Consentimiento será publicado para su descarga o copia, a través de los canales de
            comunicación
            autorizados por la UNIPOL.
        </p>
