        <p> El Programa Académico Prefacultativo tiene como finalidad capacitar y fortalecer las competencias
            cognitivas,
            físicas y
            disciplinarias de las y los postulantes en el proceso de selección y admisión, mediante lineamientos
            técnicos,
            académicos y administrativos, para facilitar su adaptación a la formación profesional policial, garantizando
            un
            proceso
            transparente, equitativo e inclusivo en el proceso de admisión a las Unidades Académicas de Pregrado de la
            UNIPOL.</p>
        <b>REQUISITOS ESPECÍFICOS PARA ACCEDER AL PROGRAMA ACADÉMICO PREFACULTATIVO</b><br />
        <p>La o el postulante que quiera acceder al Programa Académico Prefacultativo para la Admisión a las Unidades
            Académicas de
            Pregrado de la UNIPOL, deberá cumplir obligatoriamente los siguientes requisitos:</p>
        <ul class="list-unstyled pl-3">
            <li>
                <b>1.</b> Cumplir con los requisitos específicos de la convocatoria para la admisión a las unidades
                académicas
                de
                pregrado a la
                que postula y reglamentación interna de la UNIPOL.
            </li>
            <li>
                <b>2.</b> Haber aprobado o ser considerado <b><u>APTO</u></b>, en la Etapa de Evaluación Médica y la
                Etapa de
                Evaluación
                Psicológica,
                conforme el Reglamento de Admisión a las Unidades Académicas de Pregrado de la UNIPOL.
            </li>
            <li>
                <b>3.</b> Haber aprobado la etapa de Evaluación de Aptitud Física, conforme el Reglamento de Admisión a
                las
                Unidades
                Académicas
                de Pregrado de la UNIPOL.
            </li>
            <li>
                <b>4.</b> Presentar un compromiso notariado de solicitud, autorización expresa y consentimiento
