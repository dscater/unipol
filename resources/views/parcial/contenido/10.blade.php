        <p>La Evaluación Médica tiene como finalidad determinar el estado de salud integral de las y los postulantes,
            asegurando que sean aptos para cumplir con las exigencias físicas y de salud requeridas para la formación
            profesional policial, mediante evaluaciones de Medicina General, Odontología, laboratorio clínico y pruebas
            complementarias de diagnóstico.</p>
        <b>PROCEDIMIENTO DE LA EVALUACIÓN MÉDICA</b><br />
        <p>
            <b>I.</b> La Dirección Nacional de Salud y Bienestar Social es la responsable de planificar, organizar,
            desarrollar
            y
            ejecutar el proceso de la Evaluación Médica y Odontológica, en coordinación con la Comisión Nacional del
            Proceso
            de Admisión, conforme al Cronograma de Actividades, Reglamento y protocolos correspondientes.
        </p>
        <p>
            <b>II.</b> Los resultados finales de la Evaluación Médica serán expresados en términos de APTO o NO APTO,
            acompañados
            del informe médico respectivo.
        </p>
        <b>PUBLICACIÓN DE RESULTADOS</b><br />
        <p>
            <b>I.</b> La Comisión Nacional del Proceso de Admisión, conforme el cronograma de actividades publicara los
            resultados
            de la Evaluación Médica, a través de la Página Web, Redes Sociales Oficiales y/o medios de comunicación
            autorizados por la Universidad Policial.
        </p>
        <p>
            <b>II.</b> La publicación de los resultados de la Evaluación Médica no consignará el nombre de la o el
            postulante,
            solamente el número de Cédula de Identidad o código de inscripción, con la finalidad de preservar la
            privacidad
            de las y los postulantes.
        </p>
    </div>
</div>
