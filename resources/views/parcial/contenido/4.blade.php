        <p>
            <b>I.</b> Se consideran como causales de separación del proceso de admisión, los siguientes:
        </p>
        <ul class="pl-3 list-unstyled">
            <li>
                <b>a)</b> Reprobar o ser calificada o calificado como <b><u>“NO APTO”</u></b> o <b><u>“NO APTA”</u></b>
                en
                cualquiera de las Fases y/o
                etapas de la
                admisión.
            </li>
            <li>
                <b>b)</b> Cometer fraude en cualquier fase del proceso de admisión.
            </li>
            <li>
                <b>c)</b> Negarse a presentar o no presentar la documentación requerida en las diferentes fases y/o
                etapas del
                Proceso
                de Admisión.
            </li>
            <li>
                <b>d)</b> Presentar documentación falsificada, adulterada o modificada de manera irregular en su
                contenido, sin
                perjuicio de asumir las acciones legales que correspondan.
            </li>
            <li>
                <b>e)</b> No dar cumplimiento al presente Reglamento.
            </li>
            <li>
                <b>f)</b> No dar cumplimiento a las instrucciones emitidas por la Comisión Nacional, las autoridades o
                evaluadores del
                Proceso de Admisión, durante el desarrollo de éste.
            </li>
            <li>
                <b>g)</b> Ser sorprendido intentando copiar o copiando el Formulario de Evaluación, hoja de respuesta u
                otros
                instrumentos de evaluación.
            </li>
            <li>
                <b>h)</b> Suplantar su identidad en cualquiera de las fases y/o etapas de evaluación.
            </li>
            <li>
                <b>i)</b> No concluir con cualquiera de sus evaluaciones, después de iniciado el mismo, sin causa
                justificada.
            </li>
            <li>
                <b>j)</b> Tener una <u><b>NOTA MENOR A SETENTA (70,00)</b></u> puntos en la cartilla disciplinaria
            </li>
            <li>
                <b>k)</b> Extraviar, alterar modificar o destruir total o parcialmente la cartilla de Régimen
                Disciplinario del
                Prefacultativo.
            </li>
            <li>
                <b>l)</b> No presentarse a rendir cualquiera de sus evaluaciones, en la fecha y hora señaladas.
            </li>
            <li>
                <b>m)</b> Negarse a rendir cualquiera de sus evaluaciones.
            </li>
            <li>
                <b>n)</b> Presentarse a cualquiera de las fases y/o etapas del Proceso de Admisión con hálito alcohólico
                o bajo
                efecto de alguna de sustancias controladas.
            </li>
            <li>
                <b>o)</b> Injerir, consumir o suministrarse cualquier tipo de sustancia destinada a potenciar su estado
                físico
                o mental
                para rendir sus evaluaciones.
            </li>
            <li>
                <b>p)</b> Recurrir a influencias o recomendaciones de cualquier índole, antes, durante o después del
                Proceso de
                Admisión.
            </li>
            <li>
                <b>q)</b> Crear, publicar o difundir a través de medios impresos, redes sociales u otros medios de
                comunicación, textos
                o contenidos audiovisuales que carezcan de veracidad y fundamento, con la intención de desprestigiar a
                la
                Policía Boliviana, la UNIPOL o las autoridades del Proceso de Admisión.
            </li>
            <li>
                <b>r)</b> Ser denunciado por cualquier delito de orden público durante su permanencia en el programa
                académico
                del
                prefacultativo.
            </li>
            <li>
                <b>s)</b> No cumplir y/o asistir a un mínimo del ochenta por ciento (80%) de las actividades académicas
                y/o
                carga
                horaria de cada materia, asignatura o modulo del programa académico del prefacultativo.
            </li>
            <li>
                <b>t)</b> Otras acciones que no se encuentren contempladas en el presente Reglamento y que previamente
                hayan
                sido
                establecidas, comunicadas y publicadas a las y los postulantes, por la Comisión Nacional del Proceso de
                Admisión.
            </li>
        </ul>
        <p>
            <b>II.</b> En caso de que la o el postulante sea sorprendido incurriendo en alguna de las acciones señaladas
            a
            partir del inciso “b.” del acápite precedente, el hecho deberá ser comunicado de inmediato a la autoridad
            responsable del control y supervisión de la Fase y/o Etapa correspondiente del Proceso de Admisión, quien de
            manera inmediata procederá a su separación, debiendo elaborar un Acta detallada en la que especifique
            claramente la causal de la separación. Esta Acta deberá ser firmada por todas las autoridades presentes, por
            la o el postulante involucrado y por uno o más testigos de actuación. Posteriormente, el Acta deberá
            adjuntarse al Informe correspondiente
        </p>
        <p>
            <b>III.</b> La o el postulante que fuere sorprendido cometiendo una de las causales del Articulo 30, será
            separada
            o separado del Proceso de Admisión, siendo notificada (o) en el lugar al momento de firmar el Acta
            respectiva.
        </p>
        <p>
            <b>IV.</b> En caso de que la o el postulante separado del Proceso de Admisión, se rehusara a firmar el Acta
            o no
            sea
            habido, se deberá dejar registro de la negativa o ausencia de éste, en presencia y firma de un testigo.
        </p>
        <p>
            <b>V.</b> La o el postulante que sea separado de cualquiera de las Fases y/o Etapas del Proceso de Admisión,
            será
            considerado como <b><u>REPROBADA(O)</u></b> o <b><u>NO APTA(O)</u></b>.
        </p>
        <p>
            <b>VI.</b> Los informes, actas emergentes y documentación respaldatoria, deberán remitirse de forma
            inmediata por
            conducto regular a conocimiento de la Comisión Nacional del Proceso de Admisión.
        </p>
