        <p>
            Tiene por finalidad determinar el grado de resistencia, fuerza, flexibilidad, potencia y destreza de las y
            los
            postulantes, a fin de comprobar su capacidad, nivel de rendimiento y preparación física, debiendo satisfacer
            las
            marcas exigidas acordes a la formación profesional policial.
        </p>
        <b>PROCEDIMIENTO DE LA EVALUACIÓN DE APTITUD FÍSICA</b><br />
        <p>
            <b>I.</b> La Evaluación de Aptitud Física consistirá en una serie de pruebas, cada una caracterizada por un
            determinado
            tipo de ejercicios.
        </p>
        <p>
            <b>II.</b> Las pruebas de Aptitud Física, parámetros, estándares, procedimientos, desarrollo, forma de
            ejecución y
            otros aspectos, estarán previamente establecidos en Protocolos e Instructivas de Evaluación de Aptitud
            Física
            para el Proceso de Admisión, aprobado por la Comisión Nacional del Proceso de Admisión.
        </p>
        <p>
            <b>III.</b> La implementación de otras evaluaciones o pruebas adicionales, serán sujetas a determinación de
            la
            Comisión
            Nacional del Proceso de Admisión.
        </p>
        <p>
            <b>IV.</b> La Evaluación de Aptitud Física se realizará conforme a cronograma de actividades del proceso de
            admisión en
            los lugares, fechas y horarios definidos por la Comisión Nacional del Proceso de Admisión.
        </p>
        <p>
            <b>V.</b> Los <b><u>costos de la Evaluación de Aptitud Física serán cubiertos por las y los
                    postulantes</u></b>, conforme
            al
            Reglamento Interno de Tabla de Precios y Aplicación de Formularios de la Universidad Policial.
        </p>
        <p>
            <b>VI.</b> La o el postulante, previo a rendir la Evaluación de Aptitud Física, firmará un <b><u>ACTA DE
                    CONSENTIMIENTO</u></b>
            que
            deslinde de cualquier responsabilidad a la Policía Boliviana y a la UNIPOL, por cualquier lesión o daño que
            pueda sufrir durante la evaluación, asimismo, declarará formalmente que se encuentra en óptimas condiciones
            físicas y de salud para la evaluación, además de declarar no haber ingerido o consumido sustancias
            controladas o
            cualquier otro tipo de sustancia, con la finalidad de mejorar o alterar su rendimiento físico.
        </p>
        <p>
            <b>VII.</b> El Acta de Consentimiento será publicado para su descarga o copia, a través de los canales de
            comunicación
            autorizados por la UNIPOL.
        </p>
        <b>RESULTADOS FINALES DE LA EVALUACIÓN DE APTITUD FÍSICA</b><br />
        <p>
            <b>I.</b> Los resultados finales de las pruebas de la Evaluación de Aptitud Física, constarán en el
            Formulario
            Individual de Evaluación de cada postulante, donde su contenido expresará la puntuación en las respectivas
            pruebas y la nota final promediada, que determinará su aprobación o reprobación.
        </p>
        <p>
            <b>II.</b> El Formulario Individual deberá consignar la firma de la o el evaluado y del evaluador al final
            de cada
            prueba.
        </p>
        <p>
            <b>III.</b> En caso de que la o el postulante se negare a firmar el Formulario, el evaluador hará constar
            este acto
            en
            el mismo Formulario con la presencia y firma de un Testigo de Actuación.
        </p>
        <p>
            <b>IV.</b> La calificación final de la o el postulante se obtendrá del promedio de la sumatoria de los
            resultados
            de
            las pruebas evaluadas.
        </p>
        <p>
            <b>V.</b> La calificación mínima de aprobación será de cincuenta y un puntos (51,00), conforme lo
            establecido en el
            Artículo 28 del Reglamento de Admisión a las Unidades Académicas de Pregrado de la UNIPOL.
        </p>
        <b> PUBLICACIÓN DE RESULTADOS</b><br />
        <p>
            <b>I.</b> La Comisión Nacional del Proceso de Admisión, conforme el cronograma de actividades publicara los
            resultados
            la Evaluación de Aptitud Física, a través de la Página Web, Redes Sociales Oficiales y/o medios de
            comunicación
            autorizados por la Universidad Policial.
        </p>
        <p>
            <b>II.</b> La publicación de los resultados de la evaluación de aptitud física no consignará el nombre de la
            o el
            postulante, solamente el número de Cédula de Identidad o código de inscripción, con la finalidad de
            preservar la
            privacidad de las y los postulantes.

        </p>
