            de
            los promedios más altos obtenidos durante la etapa de prefacultativo.</p>
        <p>
            <b>III.</b> Las y los postulantes admitidos a las Unidades Académicas de Pregrado a Nivel de Técnico
            Superior
            (FATESCIPOL), serán asignados a nivel nacional de acuerdo a los siguientes criterios:
        </p>
        <ul class="list-unstyled ml-3">
            <li>
                <b>a)</b> Cantidad de plazas habilitadas y/o aprobadas para cada Unidad Académica de Pregrado a nivel
                nacional
                (FATESCIPOLES).
            </li>
            <li>
                <b>b)</b> Orden de prelación de los promedios más altos obtenidos durante la etapa de prefacultativo.
            </li>
        </ul>
        <p>
            <b>IV.</b> La o el Postulante que <b>NO</b> es admitido en la Unidad académica en razón de la cantidad de
            plazas
            habilitadas
            y/o aprobadas o promedio final, previo consentimiento podrá ser asignado a otra Unidad Académica de Pregrado
            (FATESCIPOL) distinta a la que postuló. En caso de que <b>NO</b> acepte su asignación de manera voluntaria y
            por
            escrito; dejará de ser tomado en cuenta en la Fase de Incorporación de la gestión correspondiente, bajo su
            responsabilidad.
        </p>
        <p>
